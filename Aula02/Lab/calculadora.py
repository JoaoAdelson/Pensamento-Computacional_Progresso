#Entrada
num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))

#Processamento
soma = num1 + num2
sub = num1 - num2
multi = num1 * num2
div = num1 / num2

#SaÃ­da
print("{} + {} = {}".format(num1, num2, soma))
print("{} - {} = {}".format(num1, num2, sub))
print("{} * {} = {}".format(num1, num2, multi))
print("{} / {} = {}".format(num1, num2, div))
